package com.company;

public class Parameters {
    protected double height;
    protected double length;
    protected double radius;
    protected double a;
    protected double b;
    protected double c;

    public Parameters(double height, double length, double radius, double a, double b, double c) {
        this.height = height;
        this.length = length;
        this.radius = radius;
        this.a = a;
        this.b = b;
        this.c = c;
    }
}
